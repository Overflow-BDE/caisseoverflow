DROP TABLE ORDER_ITEM;
DROP TABLE ORDERS;
DROP TABLE ITEMS;

CREATE TABLE ITEMS (
    ID SERIAL PRIMARY KEY UNIQUE,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    NAME VARCHAR(100),
    ICON VARCHAR(100),
    PRICE FLOAT
);

