CREATE TABLE ORDERS (
    ID SERIAL PRIMARY KEY UNIQUE,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    PAID_LYDIA FLOAT,
    PAID_CASH FLOAT,
    GVBACK_LYDIA FLOAT,
    GVBACK_CASH FLOAT
);

CREATE TABLE ORDER_ITEM (
    ID SERIAL PRIMARY KEY UNIQUE,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    ORDER_ID INTEGER REFERENCES ORDERS(ID),
    QUANTITY INTEGER
);
